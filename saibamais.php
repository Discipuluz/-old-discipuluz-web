								<h2>Sobre o questionário</h2>
								<p style="margin-top: 1em">Utilizamos em nosso trabalho a referência de Carl Jung, que em sua tipologia de personalidade buscou responder a tendências e preferências que possibilitam a descrição de um perfil de autoconhecimento. Em seu livro Tipos Psicológicos, Jung ressalta que um grupo de funções predominantes define nossa “personalidade” na primeira metade da vida, mas que justamente as funções “menos desenvolvidas” até então serão nosso desafio durante o crescimento e amadurecimento para uma vida feliz e equilibrada.</p>
								<span class="byline">Modelo Myers-Briggs baseado na Tipologia de Personalidade de Carl Jung</span>
							</header>
							<p>A teoria de Carl Jung sobre a tipologia de personalidade envolve as atitudes Introvertida e Extrovertida e as funções Sensorial/Intuitivo e Racional/Emocional.  As pesquisadoras Katharina Briggs e Isabel Myers passaram a utilizar a tipologia de personalidade de Jung para orientação profissional e adicionaram mais uma dupla de funções ligadas à estruturação ou forma de trabalho:  analítico-julgador / perspicaz-perceptivo.
No Indicador tipológico de Myers-Briggs, é gerado um perfil com quatro elementos predominantes em uma chave a partir de questionários de simples aplicação.  Não configura um teste psicológico, pois não parte de bases psicométricas e determinantes que os formatos de testes exigem, nem tampouco rigorosa tabulação de resultados.
A Tipologia de Myers-Briggs, do mesmo modo que a Tipologia de Personalidade de Jung, tem como propósito tão somente que a pessoa acesse perfis de autoconhecimento, podendo obter resultados diferentes a cada aplicação. Por este motivo a comparamos a fotografias que podem ser tiradas em períodos distintos, sendo possível a preservação de algumas características e mudanças de outras.
</p>
								<header>
									<span class="byline">Descrição das características de cada Tipo:</span>
								</header>
							<p style="color: #99D353; margin-top: 1.5em; font-weight: 400">Atitude frente ao mundo externo:   EXTROVERTIDO (E)  X INTROVERTIDO (I)</p>
							<p>A primeira dupla de características descreve como lidamos com situações novas e interação social – se escolhemos interagir imediatamente ou observar muito e depois (apenas se tivermos vontade), interagirmos. Na verdade, é mais do que isso. Os extrovertidos precisam experimentar o mundo para entendê-lo, recarregar as baterias por meio dos outros. Ao deparar-se com uma novidade, sua pergunta é: “Como eu posso mexer com essa situação?” Já os introvertidos preferem entender o mundo sozinhos.  Frente a uma nova situação, sua pergunta é: “Como isso vai mexer comigo?”</p>
							<p>Importante:  na descrição de Jung o termo INTROVERTIDO não é equivalente a tímido ou com dificuldades sociais.  Diz respeito a um modo de funcionamento que primeiro observa o entorno para depois interagir (se fizer sentido).  Geralmente os INTROVERTIDOS são os “falsos tímidos”: em situações muito novas parecem distantes e fechados; porém, quando mais ambientados não raramente assumem a liderança da comunicação ou ficam em evidência.  Tudo o que faz sentido para ele, consegue realizar – sua lógica interna comprova os fatos, não o contrário.  Os EXTROVERTIDOS guiam-se pelo o que acham ser o senso comum e o correto para a situação.  Esta é a diferença básica entre estas duas funções de personalidade.</p>
							<p style="color: #99D353; margin-top: 1.5em; font-weight: 400">Modo de captar e registrar as informações:  SENSORIAL (S)  X  INTUITIVO (N)</p>
							<p>A segunda dupla diz respeito à tendência e estilo de captação ou utilização das informações.</p>
							<p>Os Sensoriais concentram-se no que pode ser visto, ouvido, cheirado, sentido com o tato ou com o paladar. Eles acreditam apenas no que pode ser documentado ou mensurado, que lhes pareça concreto. Olham uma situação e querem descobrir o que está acontecendo exatamente. Demonstram maior facilidade para exercer funções que exijam rotina, protocolos normativos ou técnicos, pois costumam trabalhar com foco em resultados e funcionalidade.</p>
							<p>Os intuitivos usam também seus cinco sentidos, mas estão interessados em interpretações, relações, análises e possibilidades. Valorizam metáforas e compreendem com facilidade os significados simbólicos. Em geral preferem atividades que lhes permitam a liberdade de criar teorias e formas de solucionar as situações necessárias.  Não apreciam o excesso de rotinas ou protocolos preestabelecidos.  As diferenças entre esta dupla de funções se aplicam em diversas profissões – portanto torna-se um elemento importante a se levar em consideração na hora da pesquisa sobre atividades e funções exercidas.</p>
							<p style="color: #99D353; margin-top: 1.5em; font-weight: 400">Modo de julgamento:  RACIONAL (T)  X EMOTIVO (F)</p>
							<p>Esses dois tipos definem a maneira pela qual as pessoas tomam decisões e como chegam a uma conclusão sobre um assunto ou situação. Os racionais preferem tomar resoluções baseados na lógica. Tentam ser objetivos, analisam todas as evidências antes de chegar a um resultado e apostam na solução imparcial e correta a partir dos fatos. Já os emotivos decidem tendo em mente o que acham que é certo, procurando compreender as circunstâncias que levaram uma pessoa a tomar determinada atitude. Eles mantêm uma empatia com as partes envolvidas e querem que todos fiquem felizes com o resultado, sendo essencialmente conciliadores.  Apostam na solução de conflitos e do julgam ser correto para a situação e para o bem de todos.</p>
							<p style="color: #99D353; margin-top: 1.5em; font-weight: 400">ANALÍTICO ou JULGADOR (J)  X PERCEPTIVO ou PERSPICAZ (P)</p>
							<p>No indicador tipológico de Myers-Briggs encontramos a quarta dupla que refere-se ao modo como preferimos levar a vida: de modo mais estruturado ou mais espontâneo. Os analíticos-julgadores apresentam tendência para a ordem, gostam de tomar decisões e ocupar cargos de responsabilidade. Levam prazos a sério e gostam de atividades onde saibam o que deve ser feito, como deve ser feito e para quando. Os perspicazes estão sempre abertos a novas propostas, sendo mais improvisadores e muitas vezes com dificuldades para adaptarem-se a rotinas. Preferem entender a vida aproveitando os detalhes a cada momento ao invés de tentar prever  e organizar todos os detalhes.  Confiam em sua capacidade criativa e de improvisação quando necessário.</p>
